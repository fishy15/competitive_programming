void solve() {
    ll n, k; cin >> n >> k;
    if (n % 2 == 0) {
        cout << ((k - 1) % n + 1) << '\n';
    } else {
        ll half = n / 2;
        ll skip = (k - 1) / half;
        cout << ((k - 1 + skip) % n + 1) << '\n';
    }
}

int main() {
    cin.tie(0)->sync_with_stdio(0);

    int t; cin >> t;
    while (t--) solve();

    return 0;
}
