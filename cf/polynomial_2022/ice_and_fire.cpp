void solve() {
    int n;
    string s;
    cin >> n >> s;

    int cur = 1;
    cout << cur << ' ';

    char p = s[0];
    for (int i = 3; i <= n; i++) {
        if (s[i - 2] != p) {
            cur = i - 1;
        }

        cout << cur << ' ';
        p = s[i - 2];
    }
    
    cout << '\n';
}

int main() {
    cin.tie(0)->sync_with_stdio(0);

    int t;
    cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}
