using namespace std;

void solve() {
    int n, k; cin >> n >> k;
    string s; cin >> s;
    int cur = 0;

    for (int i = 0; i < n / 2; i++) {
        if (s[i] != s[n - i - 1]) {
            cur++;
        }
    }

    int ans = cur - k;
    if (ans < 0) {
        ans = -ans;
    }

    cout << ans << '\n';
}

int main() {
    cin.tie(0)->sync_with_stdio(0);

    int t; cin >> t;
    for (int i = 1; i <= t; i++) {
        cout << "Case #" << i << ": ";
        solve();
    }

    return 0;
}
