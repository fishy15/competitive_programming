using namespace std;

void solve() {
    int n; cin >> n;
    string s; cin >> s;
    int cnt = 0;
    for (char c : s) {
        if (c == 'A') cnt++;
    }

    if (cnt + 1 == n - cnt || cnt - 1 == n - cnt) {
        cout << "Y\n";
    } else {
        cout << "N\n";
    }
}

int main() {
    cin.tie(0)->sync_with_stdio(0);

    int t; cin >> t;
    for (int i = 1; i <= t; i++) {
        cout << "Case #" << i << ": ";
        solve();
    }

    return 0;
}
