using namespace std;

void solve() {
    int n; cin >> n;
    string s; cin >> s;
    string s2;

    for (char c : s) {
        if (c != 'F' && (s2.empty() || s2.back() != c)) {
            s2 += c;
        }
    }

    if (s2.empty()) {
        cout << "0\n";
    } else {
        cout << s2.size() - 1 << '\n';
    }
}

int main() {
    cin.tie(0)->sync_with_stdio(0);

    int t; cin >> t;
    for (int i = 1; i <= t; i++) {
        cout << "Case #" << i << ": ";
        solve();
    }

    return 0;
}
