using namespace std;

void solve() {
    ll w, g, l;
    cin >> w >> g >> l;
    auto steps = (w - g) % MOD;
    auto mul = (2 * l + 1) % MOD;
    auto ans = steps * mul % MOD;
    cout << ans << '\n';
}

int main() {
    cin.tie(0)->sync_with_stdio(0);

    int t;
    cin >> t;

    for (int i = 1; i <= t; i++) {
        cout << "Case #" << i << ": ";
        solve();
    }

    return 0;
}
