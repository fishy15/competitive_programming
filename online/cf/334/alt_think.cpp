using namespace std;

int n;
string s;

int main() {
    cin.tie(0); ios::sync_with_stdio(0);

    cin >> n >> s;

    char cur = s[0];

    int len = 1;
    int doub = 0;
    for (int i = 1; i < n; i++) {
        if (s[i] != cur) {
            len++;
            cur = s[i];
        }

        if (s[i - 1] == s[i]) {
            doub++;
        }
    }

    cout << len + min(doub, 2) << '\n';

    return 0;
}
