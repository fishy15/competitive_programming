using namespace std;

void solve() {
    ll x, y; cin >> x >> y;
    ll sq = sqrt(x + 1);
    ll ans = 0;
    for (ll i = 1; i <= sq; i++) {
        int lo = min(i + 1, y + 1);
        int hi = min((x - i) / i, y);
        ans += max(hi - lo + 1, 0);
    }
    cout << ans << '\n';
}

int main() {
    cin.tie(0)->sync_with_stdio(0);

    int t; cin >> t;
    while (t--) solve();

    return 0;
}
