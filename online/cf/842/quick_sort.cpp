using namespace std;

void solve() {
    int n, k;
    cin >> n >> k;

    vector<int> nums(n);
    for (int i = 0; i < n; i++) {
        cin >> nums[i];
    }

    int in_order = 0;
    for (int x : nums) {
        if (x == in_order + 1) {
            in_order++;
        }
    }

    int rest = n - in_order;
    cout << (rest + k - 1) / k << '\n';
}

int main() {
    cin.tie(0)->sync_with_stdio(0);

    int t;
    cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}
