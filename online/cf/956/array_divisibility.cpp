void solve() {
    int n;
    cin >> n;

    vector<int> ans(n);
    for (int i = n; i >= 1; i--) {
        int cur_sum = 0;
        for (int j = 1; j <= i; j++) {
            if (j % i == 0) {
                cur_sum += ans[j-1];
            }
        }

        ans[i-1] = i - (cur_sum % i);
    }

    for (auto x : ans) {
        cout << x << ' ';
    }
    cout << '\n';
}

int main() {
    cin.tie(0)->sync_with_stdio(0);

    int t;
    cin >> t;

    while (t--) {
        solve();
    }

    return 0;
}
