using namespace std;

int first_one(string s) {
    auto res = s.find('1');
    if (res == string::npos) {
        return sz(s);
    }
    return (int) res;
}

void solve() {
    int n;
    cin >> n;
    string s, t;
    cin >> s >> t;
    if (first_one(s) <= first_one(t)) {
        cout << "YES\n";
    } else {
        cout << "NO\n";
    }
}

