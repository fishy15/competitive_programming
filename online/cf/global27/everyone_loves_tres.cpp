void solve() {
    // ends in 6, alternating sum is 0
    int n;
    cin >> n;

    if (n == 1 || n == 3) {
        cout << "-1\n";
    } else if (n % 2 == 0) {
        string ans = string(n - 2, '3') + "66";
        cout << ans << '\n';
    } else {
        string ans = string(n - 4, '3') + "6366";
        cout << ans << '\n';
    }
}

