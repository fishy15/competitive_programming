bool check(int n) {
    for (int i = 2; i <= sqrt(n); i++) {
        if (n % i == 0) {
            return false;
        }
    }

    return true;
}

int main() {
    cin.tie(0); ios::sync_with_stdio(0);

    int n; cin >> n;
    vector<int> connect;

    for (int i = 0; i < n; i++) {
        connect.push_back(2);
    }

    int pos = 0;
    while (prime[pos] < n) {
        pos++;
    }

    int diff = prime[pos] - n;

    int cnt = 0;
    int i = 0;

    while (cnt < diff) {
        while (i < n && connect[i] == 3) {
            i++;
        }

        connect[i]++;
        connect[i + 2]++;
        i++;
        cnt++;
    }

    cout << prime[pos] << '\n';
    for (int i = 0; i < n; i++) {
        cout << (i + 1) << ' ' << (i + 1) % n + 1 << '\n';
        if (connect[i] == 3) {
            cout << i + 1 << ' ' << i + 3 << '\n';
            connect[i + 2]--;
        }
    }

    return 0;
}
