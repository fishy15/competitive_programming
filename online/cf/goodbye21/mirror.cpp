using namespace std;

void solve() {
    int n; cin >> n;
    string s; cin >> s;

    string ans;
    for (char c : s) {
        if (ans.empty()) {
            ans += c;
        } else if (c < ans[0] && c <= ans.back()) {
            ans += c;
        } else {
            break;
        }
    }

    cout << ans;
    reverse(ans.begin(), ans.end());
    cout << ans << '\n';
}

int main() {
    cin.tie(0)->sync_with_stdio(0);

    int t; cin >> t;
    while (t--) solve();

    return 0;
}
