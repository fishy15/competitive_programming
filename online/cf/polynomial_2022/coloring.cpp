void solve() {
    int n, m, k;
    cin >> n >> m >> k;

    vector<int> a(m);
    for (int i = 0; i < m; i++) {
        cin >> a[i];
    }

    sort(a.begin(), a.end(), greater<int>());

    for (int i = 0; i < m; i++) {
        int blocks = (n + k - i - 1) / k;
        if (a[i] > blocks) {
            cout << "NO\n";
            return;
        }
    }

    cout << "YES\n";
}

