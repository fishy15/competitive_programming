void solve() {
    int n; cin >> n;
    vector<int> nums(n);
    for (int i = 0; i < n; i++) {
        cin >> nums[i];
    }
    sort(nums.begin(), nums.end());
    ll sum = 0;
    for (int i = 0; i < n; i++) {
        if (nums[i] > i + 1) {
            cout << "Second\n";
            return;
        } else {
            sum += nums[i] - i - 1;
        }
    }
    if (sum % 2 == 0) {
        cout << "Second\n";
    } else {
        cout << "First\n";
    }
}

int main() {
    cin.tie(0)->sync_with_stdio(0);

    int t; cin >> t;
    while (t--) solve();

    return 0;
}
