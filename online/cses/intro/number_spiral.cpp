void solve() {
    ll x, y; cin >> x >> y;
    swap(x, y);
    ll min_square = max(x, y);
    ll start = (min_square - 1) * (min_square - 1);

    if (min_square % 2 == 1) {
        if (x <= y) {
            cout << start + x << '\n';
        } else {
            cout << min_square * min_square - y + 1 << '\n';
        }
    } else {
        if (y <= x) {
            cout << start + y << '\n';
        } else {
            cout << min_square * min_square - x + 1 << '\n';
        }
    }
}

int main() {
    cin.tie(0); ios::sync_with_stdio(0);

    int t; cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
