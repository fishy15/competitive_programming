#define MAXN 200010

using namespace std;

int n, q;
ll pre[MAXN];

int main() {
    cin.tie(0)->sync_with_stdio(0);

    cin >> n >> q;
    for (int i = 1; i <= n; i++) {
        cin >> pre[i];
        pre[i] += pre[i - 1];
    }

    for (int i = 0; i < q; i++) {
        int a, b; cin >> a >> b;
        cout << pre[b] - pre[a - 1] << '\n';
    }

    return 0;
}
