// change if necessary
#define MAXN 1000010

using namespace std;

ll MOD;

struct mi {
    int v;
    mi() : mi(0) {}
    mi(int _v) : v(_v) {
        if (v >= MOD) v -= MOD;
        if (v < 0) v += MOD;
    }
    mi(ll _v) : mi((int)(_v % MOD)) {}
    mi operator+(const mi &m2) const { return mi(v + m2.v); }
    mi operator-(const mi &m2) const { return mi(v - m2.v); }
    mi operator*(const mi &m2) const { return mi((ll) v * m2.v); }
    mi operator/(const mi &m2) const { return mi((ll) v * m2.inv().v); }
    mi &operator+=(const mi &m2) { return *this = *this + m2; }
    mi &operator-=(const mi &m2) { return *this = *this - m2; }
    mi &operator*=(const mi &m2) { return *this = *this * m2; }
    mi &operator/=(const mi &m2) { return *this = *this / m2; }
    mi pow(ll e) const {
        mi res = 1;
        mi n = *this;
        while (e > 0) {
            if (e & 1) res *= n;
            n *= n;
            e >>= 1;
        }
        return res;
    }
    mi inv() const {
        return pow(MOD - 2);
    }
};

string n;
mi pre[MAXN];
map<int, int> cnt;

int main() {
    cin.tie(0)->sync_with_stdio(0);

    cin >> n >> MOD;
    reverse(n.begin(), n.end());

    mi ten = 1;
    for (int i = 0; i < (int) n.size(); i++) {
        pre[i + 1] += pre[i];
        pre[i + 1] += ten * (n[i] - '0');
        ten *= 10;
    }

    for (int i = 0; i <= (int) n.size(); i++) {
        cnt[pre[i].v]++;
    }
    
    ll ans = 0;
    
    for (auto [_, c] : cnt) {
        ans += (ll) c * (c - 1) / 2;
    }

    cout << ans << '\n';

    return 0;
}
